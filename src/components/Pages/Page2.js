// THIS COMPONENT CONTAIN ONLY SORTING AND COLUMN FILTERING

import React, { useMemo } from "react";
import {
  useTable,
  useSortBy,
  useFilters,


} from "react-table";
import MOCK_DATA from "../MOCK_DATA (1).json";
// import { GROUPED_COLUMNS} from "./columns";  //for grouping purposes use this
import { COLUMNS } from "../columns";
import "../table.css";


function Page2() {
  //here usememo hook make sure that the colums and data is not creted every time the component is rendered because dependency is an empty array
  //   const columns = useMemo(() => GROUPED_COLUMNS, []); //for grouping purposes use this
  const columns = useMemo(() => COLUMNS, []);
  const data = useMemo(() => MOCK_DATA, []);

  const {
    getTableProps,
    getTableBodyProps,
    headerGroups,
    footerGroups,
    rows,
    prepareRow,
  } = useTable(
    {
      columns,
      data,
    },
    useFilters, 
    useSortBy,
  ); 

  return (
    <>
      <table {...getTableProps()}>
        <thead>
          {headerGroups.map((headerGroup) => (
            <tr {...headerGroup.getHeaderGroupProps()}>
              {headerGroup.headers.map((column) => (
                <th {...column.getHeaderProps(column.getSortByToggleProps())}>
                  {column.render("Header")}
                  <span>
                    {column.isSorted
                      ? column.isSortedDesc
                        ? " ðŸ”½"
                        : " ðŸ”¼"
                      : ""}
                  </span>
                  <div>{column.canFilter ? column.render("Filter") : null}</div>
                </th> //header is from COLUMNS section from "./columns"
              ))}
            </tr>
          ))}
        </thead>
        <tbody {...getTableBodyProps()}>
          {rows.map((row) => {
            prepareRow(row);
            return (
              <tr {...row.getRowProps()}>
                {row.cells.map((cell) => {
                  return (
                    <td {...cell.getCellProps()}>{cell.render("Cell")}</td>
                  );
                })}
              </tr>
            );
          })}
        </tbody>

        <tfoot>
          {footerGroups.map((footerGroup) => (
            <tr {...footerGroup.getFooterGroupProps()}>
              {footerGroup.headers.map((column) => (
                <td {...column.getFooterProps}>{column.render("Footer")}</td>
              ))}
            </tr>
          ))}
        </tfoot>
      </table>
    </>
  );
}

export default Page2;




  